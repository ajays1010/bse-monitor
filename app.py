import threading
import time
import schedule
from flask import Flask
from monitor4 import check_for_new_announcements_task  # your existing logic

app = Flask(__name__)

# â”€â”€â”€ Worker loop â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
def worker_loop():
    """Run once immediately, then every 5 minutes."""
    check_for_new_announcements_task()
    schedule.every(5).minutes.do(check_for_new_announcements_task)
    while True:
        schedule.run_pending()
        time.sleep(1)

# â”€â”€â”€ Kick off the thread at import time â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
threading.Thread(target=worker_loop, daemon=True).start()
app.logger.info("ğŸ§µ Background worker thread started")

